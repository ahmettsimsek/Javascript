let count = 10

while(count > 0){
    count-- // count=count-1
    console.log(count)
}

// for ve while döngüleri koşul, döngü başlamadan önce kontrol edilir. Do-While döngüsünde ise bu kontrol her döngüden sonra gerçekleşir. Operasyon mantığında do while döngüsü, koşul ne olursa olsun en az 1 kere çalışır. 